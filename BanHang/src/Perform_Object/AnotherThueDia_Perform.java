package Perform_Object;

public class AnotherThueDia_Perform {
	private String MaBangDia;
	private String TenBangDia;
	private String NoiDung;
	public AnotherThueDia_Perform(String maBangDia, String tenBangDia, String noiDung) {
		super();
		MaBangDia = maBangDia;
		TenBangDia = tenBangDia;
		NoiDung = noiDung;
	}
	public AnotherThueDia_Perform() {}
	public String getMaBangDia() {
		return MaBangDia;
	}
	public void setMaBangDia(String maBangDia) {
		MaBangDia = maBangDia;
	}
	public String getTenBangDia() {
		return TenBangDia;
	}
	public void setTenBangDia(String tenBangDia) {
		TenBangDia = tenBangDia;
	}
	public String getNoiDung() {
		return NoiDung;
	}
	public void setNoiDung(String noiDung) {
		NoiDung = noiDung;
	}
	
}
